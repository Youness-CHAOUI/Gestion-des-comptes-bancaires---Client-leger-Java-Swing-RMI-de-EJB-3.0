    private void jTabbedPane1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MouseClicked
        int index = jTabbedPane1.getSelectedIndex();
        if(index == 0)
        {
            /* Remplire la table */
            DefaultTableModel model = (DefaultTableModel) tablesComptes.getModel();
            while (model.getRowCount() > 0) model.removeRow(0);
            listeComptes = eBanque.consulterComptes();
            for(Compte c : listeComptes)
            {   
                String numCompte = c.getNumCompte();
                Double s = c.getSolde();
                Object decouvert=null;
                Object taux=null;
                if(c instanceof CompteCourant)
                {
                    CompteCourant cc = (CompteCourant)c;
                    decouvert = cc.getDecouvert();
                }
                else
                {
                    CompteEpargne ce = (CompteEpargne)c;
                    taux = ce.getTaux();
                }  
                Date d = c.getDateCreation();
                Long numEmp = c.getEmploye().getNumEmploye();
                Long codeClient = c.getClient().getCodeClient();
                model.addRow(new Object[]{numCompte, s, taux, decouvert, d, codeClient, numEmp});
            }
        }
        else if(index == 1)
        {
            comptesComboBox.removeAllItems();
            listeComptes = eBanque.consulterComptes();
            for(Compte c : listeComptes)
                comptesComboBox.addItem(c.getNumCompte());
        }
        else if(index == 2)
        {
            comptesComboBox1.removeAllItems();
            listeComptes = eBanque.consulterComptes();
            for(Compte c : listeComptes)
                comptesComboBox1.addItem(c.getNumCompte());
        }
        else if(index == 3)
        {
            listeComptes = eBanque.consulterComptes();
            comptesComboBox2.removeAllItems();
            comptesComboBox3.removeAllItems();
            for(Compte c : listeComptes)
            {
                comptesComboBox2.addItem(c.getNumCompte());
                comptesComboBox3.addItem(c.getNumCompte());
            }
        }
    }//GEN-LAST:event_jTabbedPane1MouseClicked

    private void verserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verserButtonActionPerformed
        try
        {   
            Double mnt = Math.abs(Double.parseDouble(montantTextBox.getText()));
            if(comptesComboBox.getSelectedIndex()<0)
            {
                JOptionPane.showMessageDialog(null, "\nVeuillez choisir un compte !\n\n", "ATTENTION", JOptionPane.WARNING_MESSAGE);
                return;
            }
            String compte = comptesComboBox.getSelectedItem().toString();
            Operation opV = new Versement(new Date(), mnt);
            eBanque.addOperation(opV, compte, EMPLOYE.getNumEmploye());
            montantTextBox.setText("");
            codeClientTextBox.setText("");
            JOptionPane.showMessageDialog(null, "\nL'opération de versement est passée avec succes.\n\n", "INFO", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(NumberFormatException ex)
        {
            montantTextBox.setForeground(Color.WHITE);
            montantTextBox.setBackground(Color.RED);
            JOptionPane.showMessageDialog(null, "\nVeuillez saisir un réel pour le montant.\n\n", "ATTENTION", JOptionPane.WARNING_MESSAGE);
            montantTextBox.setText("");
        }        
    }//GEN-LAST:event_verserButtonActionPerformed

