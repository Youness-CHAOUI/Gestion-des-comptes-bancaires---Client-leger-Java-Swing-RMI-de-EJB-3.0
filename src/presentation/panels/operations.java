    private void codeClientTextBoxKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codeClientTextBoxKeyReleased
        if(codeClientTextBox.getText().isEmpty())
        {   
            codeClientTextBox.setForeground(Color.BLACK);
            codeClientTextBox.setBackground(Color.WHITE);
            NomClientLabel.setText("");
            comptesComboBox.removeAllItems();
            listeComptes = Authentification_Form.eBanque.consulterComptes();
            for(Compte c : listeComptes)
                comptesComboBox.addItem(c.getNumCompte());
        }
        else
        {   
            try
            {   
                codeClientTextBox.setForeground(Color.BLACK);
                codeClientTextBox.setBackground(Color.WHITE);
                Long codeClient = Long.parseLong(codeClientTextBox.getText());
                comptesComboBox.removeAllItems();
                Client cl = eBanque.consulterClient(codeClient);
                NomClientLabel.setText(cl.getNomClient());
                listeComptes = cl.getComptes();
                for(Compte c : listeComptes)
                    comptesComboBox.addItem(c.getNumCompte());
            }
            catch(NumberFormatException ex)
            {   
                codeClientTextBox.setForeground(Color.WHITE);
                codeClientTextBox.setBackground(Color.RED);
                JOptionPane.showMessageDialog(null, "\nVeuillez saisir un nombre.\n\n", "ATTENTION", JOptionPane.WARNING_MESSAGE);
                NomClientLabel.setText("");
                codeClientTextBox.setForeground(Color.BLACK);
                codeClientTextBox.setBackground(Color.WHITE);
            }
            catch(RuntimeException ex)
            {   
                NomClientLabel.setText("");
                codeClientTextBox.setForeground(Color.WHITE);
                codeClientTextBox.setBackground(Color.RED);
            }
        }
        
    }//GEN-LAST:event_codeClientTextBoxKeyReleased

